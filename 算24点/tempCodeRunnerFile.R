#' @description 4位整数通过四则运算算24点
#' @example cal24(c(1,2,3,4))
#' @author lw
cal24 <- function(numbers) {
    operators <- c("+", "-", "*", "/")
    operators_com <- expand.grid(operators, operators, operators)
    numbers_com <- perm(numbers)
    for (i in seq_len(nrow(numbers_com))) {
        # 所有情况遍历
        formulas <- paste0(numbers_com[i, 1], operators_com[, 1], numbers_com[i, 2], operators_com[, 2], numbers_com[i, 3], operators_com[, 3], numbers_com[i, 4])
        # 考虑括号
        formulas <- c(
            formulas,
            paste0("(", numbers_com[i, 1], operators_com[, 1], numbers_com[i, 2], ")", operators_com[, 2], numbers_com[i, 3], operators_com[, 3], numbers_com[i, 4]), # (a+b)*c+d
            paste0("(", numbers_com[i, 1], operators_com[, 1], numbers_com[i, 2], ")", operators_com[, 2], "(", numbers_com[i, 3], operators_com[, 3], numbers_com[i, 4], ")"), # (a+b)*(c+d)
            paste0("(", numbers_com[i, 1], operators_com[, 1], numbers_com[i, 2], operators_com[, 2], numbers_com[i, 3], ")", operators_com[, 3], numbers_com[i, 4]), # (a+b+c)*d
            paste0(numbers_com[i, 1], operators_com[, 1], "(", numbers_com[i, 2], operators_com[, 2], "(", numbers_com[i, 3], operators_com[, 3], numbers_com[i, 4], "))") # a*(b+c+d)
        )
        browser()
        for (j in seq_along(formulas)) {
            result <- eval(parse(text = formulas[j]))
            if (!is.na(result) && abs(result - 24) < 1e-3) {
                print(formulas[j])
            }
        }
    }
    message(paste(numbers, collapse = ","), "无解.")
}

# https://www.r-bloggers.com/2019/06/learning-r-permutations-and-combinations-with-base-r/
perm <- function(v) {
    n <- length(v)
    if (n == 1) {
        v
    } else {
        X <- NULL
        for (i in 1:n) X <- rbind(X, cbind(v[i], perm(v[-i])))
        X
    }
}
